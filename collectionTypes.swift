import Foundation

// Arrays Sets and Dictionarys

// Empty array
var emptyArray = [String]()
print(emptyArray)

// Array with double numbers
var doubleNumber = [Double]()
doubleNumber = [10.5, 11.4, 12.5, 13.6]
print(doubleNumber)

// Array of strings
var arrayStrings = [String]()
arrayStrings = ["Emerson", "Denzyl", "Wouter", "Kishen"]
print(arrayStrings)

// Dictionary

var arrayDictionary = [String: String]()
arrayDictionary = ["rock": "Metallica", "pop": "NSync", "rap": "NWA"]
print(arrayDictionary)

// Sets
// Empty set

var emptySet = Set<String>()
emptySet = []
print(emptySet)

// Set with strings

var stringSet = Set<String>()
stringSet = ["meme", "kaka", "Danni"]
print(stringSet)

// set with integers

var integerSet = Set<Int, Int>()
integerSet = [1, 2, 3, 4, 5, 6]
print(integerSet)
